CREATE TABLE customers(
	id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    firstname VARCHAR(20) NOT NULL,
    lastname VARCHAR(20) NOT NULL,
    fullname VARCHAR(40) AS (CONCAT(firstname,' ',lastname)) VIRTUAL,
